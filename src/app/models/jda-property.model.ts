export interface JDAProperty {
  /**
   * The address of the property.
   */
  address?: string;
  /**
   * The city where is the property.
   */
  city?: string;
  /**
   * The ID of the property.
   */
  id?: number;
  /**
   * The inventary number for the property.
   */
  inventary?: number;
  /**
   * The ministry that use the property.
   */
  ministry?: string;
  /**
   * The last modified date of the record for the properity.
   */
  modified?: Date;
  /**
   * The name of the property.
   */
  name?: string;
  /**
   * The state where is the property.
   */
  state?: string;
  /**
   * Type of the property.
   */
  type?: string;
  /**
   * The using of the property.
   */
  using?: string;
}
